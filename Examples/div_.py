print('=-'*50)
for i in range(50):
    i -= 25
    print(f'{i} // 10 = ', i // 10, f'{i} % 10 = ', i % 10)

# i = (i // k) * k + (i % k)
print((12 // 7) * 7 + (12 % 7))  # 12
print((-12 // 7) * 7 + (-12 % 7))  # -12
print((12 // -7) * -7 + (12 % -7))  # -12
